<p><span class="capWord">URBAN TEACHER CENTER (UTC)</span> founders, <span class="capWord">JENNIFER GREEN</span> and <span class="capWord">CHRISTINA HALL</span> led parallel lives before meeting, each having worked as teachers, at local education funds, and at advocacy organizations in Boston and Baltimore.

<p>Christina was an attorney and child advocate before switching to teaching to reach children earlier in their lives. Jennifer was a TFA corps member in New Orleans, where she was selected as a new teacher of the year in her county. After getting her M. ED from Harvard, she went on to a senior leadership role in the Baltimore City school system.</p>

<p>Eventually, the two women became acquainted. It was Jennifer who first proposed they create an organization to train teachers who'd start their careers prepared for the challenge of the classroom and ready to take full responsibility for student outcomes. Both agreed that our nation was not taking the measures necessary, such as giving prospective teachers hands-on hours in the classroom, to ensure that every child had an excellent teacher, every year. A year later, Christina invited Jennifer to lunch saying, "I can't get your idea out of my mind!" The result is UTC, a teacher residency program that recruits outstanding teacher candidates, equips them with state-of-the-art training, and links certification to student performance results.</p>

<p>"<span class="capWord">NEWSCHOOLS</span> became our first ally and first funded us in 2009," said Christina. "Without their ongoing financial, intellectual and networking support we never would have had the opportunity to create this breakthrough teacher preparation model."</p>

<p>Today, Urban Teacher Center revolves around the belief that schools need a guarantee— before teachers graduate and are licensed, they must demonstrate they can deliver quality instruction and that their students can show achievement gains. Since 2009, UTC has launched four cohorts and is training 314 teacher residents in 75 schools in Baltimore and Washington, DC.</p>
                            
