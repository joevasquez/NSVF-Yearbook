<p><span class="capWord">For Rena</span>, growing up in East Oakland was not always easy. When her relationship with her mother became strained, she was raised by her 15-year-old cousin. Rena sought mentorship from her teachers at Aspire to augment what was missing in her home. "In the beginning I was quite stubborn and a lazy student," Rena said. But her 8th grade teacher, Megan Reed, saw potential in her, assigning books like "The Poisonwood Bible" to stretch her. "I needed to be challenged and they gave me opportunities that would continue to push me."</p>

<p>At Aspire, all students are expected to attend college, and Rena applied to 21 colleges. When the acceptance letter finally arrived from Fisk University in Nashville, TN, Rena immediately put the letter in her handbag and took the train over to the school. When she showed the letter to Ms. Reed, her teacher promptly photocopied it and hung it on her wall.</p>

<p><span class="capWord">Today</span>, Rena has just completed her first year of teacher residency at Match Education in Boston. She will teach fourth grade English at UP Holland this upcoming fall, 2014, and looks forward to serving as a mentor to her students just as her teachers at Aspire did for her. "I definitely know I want to be a mentor to my students. And to communicate to them that anything is possible."</p>
    
                            
